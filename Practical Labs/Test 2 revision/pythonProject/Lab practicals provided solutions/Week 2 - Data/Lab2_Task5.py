# Define the constant for speed in miles per hour
SPEED = 60  # miles per hour

# Calculate the distances for different time intervals
distance_5_hours = SPEED * 5
distance_8_hours = SPEED * 8
distance_12_hours = SPEED * 12

# Display the results
print("The car will travel the following distances:")
print(f"{distance_5_hours} miles in 5 hours.")
print(f"{distance_8_hours} miles in 8 hours.")
print(f"{distance_12_hours} miles in 12 hours.")
